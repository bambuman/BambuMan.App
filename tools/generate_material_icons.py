"""
Regenerates the self-hosted Material Symbols (Sharp) icon font + C# glyph constants.

Pipeline:
  1. Instance the variable Material Symbols Sharp font to a static wght=400 face
     (so it renders correctly on Android API 23-25, which can't vary fonts).
  2. Subset it to the curated ICONS list below (keeps the .ttf a few KB instead of 8 MB).
  3. Emit src/BambuMan/Resources/Icons/MaterialSharp.cs with one `public const string`
     per icon, named first-letter-capitalized to match the existing XAML
     (e.g. {x:Static m:MaterialSharp.Qr_code_scanner}).

To add an icon: add its snake_case name to ICONS and re-run:
  python tools/generate_material_icons.py <path-to>.codepoints

Source font/codepoints: google/material-design-icons (Apache-2.0), variablefont/MaterialSymbolsSharp.
"""
import sys, os
from fontTools.ttLib import TTFont
from fontTools.varLib.instancer import instantiateVariableFont
from fontTools import subset

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
FONT_SRC = os.path.join(REPO, "src", "BambuMan", "Resources", "Fonts", "MaterialSymbolsSharp.ttf")
CS_OUT   = os.path.join(REPO, "src", "BambuMan", "Resources", "Icons", "MaterialSharp.cs")

# Curated icon set: everything the app uses today + design-review / deferred-plan icons + common extras.
ICONS = [
    # currently used (Material Symbols)
    "house", "settings", "bug_report", "link", "qr_code_scanner", "key", "info",
    "calendar_month", "sell", "tag", "map", "refresh", "arrow_back", "science",
    "warning", "weight", "settings_backup_restore",
    # replaces FontAwesome (gear->settings, house->house, circle->circle)
    "circle",
    # status / cheap fixes
    "check_circle", "error", "cancel", "brightness_1", "check", "close",
    # design-review + deferred (main/logs/scan)
    "nfc", "contactless", "inbox", "flash_on", "flash_off", "search",
    "add", "delete", "edit", "content_copy", "visibility", "visibility_off",
    "dark_mode", "light_mode", "location_on", "place", "scale", "straighten",
    "attach_money", "label", "sync", "help", "expand_more", "chevron_right",
    "arrow_forward", "menu", "more_vert", "share", "download", "upload",
    "wifi", "wifi_off", "print", "inventory_2", "category", "palette",
]


def load_codepoints(path):
    cp = {}
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            name, hexcode = line.split()
            cp[name] = int(hexcode, 16)
    return cp


def member(name):
    return name[0].upper() + name[1:]


def main():
    if len(sys.argv) < 2:
        sys.exit("usage: generate_material_icons.py <codepoints-file>")
    cp = load_codepoints(sys.argv[1])

    wanted, missing = {}, []
    for name in ICONS:
        if name in cp:
            wanted[name] = cp[name]
        else:
            missing.append(name)
    if missing:
        print("WARNING: not found in codepoints (skipped):", ", ".join(missing))

    # 1. instance variable -> static wght 400
    font = TTFont(FONT_SRC)
    if "fvar" in font:
        axes = {a.axisTag: a.defaultValue for a in font["fvar"].axes}
        axes["wght"] = 400
        if "FILL" in axes: axes["FILL"] = 0
        if "GRAD" in axes: axes["GRAD"] = 0
        instantiateVariableFont(font, axes, inplace=True)

    # 2. subset to wanted glyphs
    opts = subset.Options()
    opts.glyph_names = False
    opts.name_IDs = ["*"]
    opts.recalc_bounds = True
    ss = subset.Subsetter(options=opts)
    ss.populate(unicodes=list(wanted.values()))
    ss.subset(font)
    font.save(FONT_SRC)
    print("font: %s (%d glyphs, %.1f KB)" % (
        os.path.basename(FONT_SRC), len(wanted), os.path.getsize(FONT_SRC) / 1024))

    # 3. emit the C# constants
    os.makedirs(os.path.dirname(CS_OUT), exist_ok=True)
    lines = [
        "// <auto-generated />",
        "// Material Symbols (Sharp) glyph constants — self-hosted replacement for",
        "// UraniumUI.Icons.MaterialSymbols. Regenerate with tools/generate_material_icons.py.",
        "namespace BambuMan.Resources.Icons;",
        "",
        "/// <summary>Material Symbols (Sharp) glyphs. Use as {x:Static m:MaterialSharp.Name}.</summary>",
        "public static class MaterialSharp",
        "{",
        '    public const string FontFamily = "MaterialSharp";',
        "",
    ]
    for name in sorted(wanted):
        lines.append('    public const string %s = "\\u%04x";' % (member(name), wanted[name]))
    lines.append("}")
    with open(CS_OUT, "w", encoding="utf-8", newline="\r\n") as f:
        f.write("\n".join(lines) + "\n")
    print("class: %s (%d members)" % (os.path.relpath(CS_OUT, REPO), len(wanted)))


if __name__ == "__main__":
    main()
